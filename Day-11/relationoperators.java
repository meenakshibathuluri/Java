import java.util.Scanner;
public class relationaloperator {
    public static void main(String[] args) {
        int num1 = 47;
        int num2 = 30;
        System.out.println(num1 == num2);
        num2 = 47;
        System.out.println(num1 == num2);
        num1 = 30;
        System.out.println(num1 == num2);
        int num3 = 62;
        int num4 = 82;
        System.out.println(num3 == num4);
        num4 = 62;
        System.out.println(num3 == num4);
    }
}