import java.util.Scanner;
public class arithmeticoperators {
    public static void main(String[] args) {
        int num1 = 46;
        int num2 = 30;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
    }
}