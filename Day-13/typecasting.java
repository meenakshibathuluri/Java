import java.util.Scanner;
public class typecasting {
    public static void main(String[] args) {
        int num = 17;
        byte b1 = (byte)num;
        System.out.println(b1);
        double d1 = 35;
        long l1 = (long)d1;
        System.out.println(l1);
        float f1 = 48.0f;
        short s1 = (short)f1;
        System.out.println(s1);
    }
}
